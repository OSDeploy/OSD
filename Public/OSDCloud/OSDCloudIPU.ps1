    {
    $SetupProgress = Get-ItemPropertyValue -Path $SetupProgressPath -Name "SetupProgress" -ErrorAction SilentlyContinue
    if (!($SetupProgress))
        {
        $Minutes += 1
        Start-Sleep -Seconds 60
        }
